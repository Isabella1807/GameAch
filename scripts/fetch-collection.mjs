// Fetch a Coral Island wiki category's members + icon images and write
// src/data/items/<key>.ts as a CollectionItem[].
//
// Usage: node scripts/fetch-collection.mjs "<Category>" <key>
//   e.g. node scripts/fetch-collection.mjs Fish fish
//        node scripts/fetch-collection.mjs "Crafted with crafting menu" craftables
//        node scripts/fetch-collection.mjs "Animal products" animal-products
//
// Source: Coral Island Wiki (Fandom) MediaWiki API. Images are hotlinked from the Fandom CDN.

import { writeFileSync, mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const [category, key] = process.argv.slice(2)
if (!category || !key) {
  console.error('Usage: node scripts/fetch-collection.mjs "<Category>" <key>')
  process.exit(1)
}

// The file is named <key>.ts but the export uses a valid camelCase identifier
// ("animal-products" -> "animalProducts").
const varName = key.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase())

const api =
  `https://coralisland.fandom.com/api.php?action=query&generator=categorymembers` +
  `&gcmtitle=Category:${encodeURIComponent(category)}&gcmlimit=500` +
  `&prop=pageimages&piprop=original&pilimit=500&format=json`

const res = await fetch(api, {
  headers: { 'User-Agent': 'gameach-data-fetch/1.0 (personal Coral Island achievement tracker)' },
})
if (!res.ok) {
  console.error(`HTTP ${res.status} for Category:${category}`)
  process.exit(1)
}
const data = await res.json()
const pages = data?.query?.pages ?? {}

const slug = (n) =>
  n
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')

// Skill/mechanic pages that carry a collection's category tag on the wiki but aren't
// collectible items (e.g. Category:Foraging includes the "Foraging" skill page and the
// "Trees" mechanic page). Keep this list of exact page titles out of the generated data.
const DENY = new Set([
  'Foraging', 'Trees', 'Fishing', 'Diving', 'Mining', 'Farming', 'Ranching',
  'Cooking', 'Crafting', 'Combat', 'Mastery', 'Animals', 'Bug catching', 'Insects',
])

const items = Object.values(pages)
  .filter(
    (p) =>
      p.title && !p.title.includes('/') && !p.title.startsWith('Category:') && !DENY.has(p.title),
  )
  .map((p) => ({ name: p.title, image: p.original?.source ?? null }))
  .sort((a, b) => a.name.localeCompare(b.name))

if (items.length === 0) {
  console.error(`No items found for Category:${category} — check the category name.`)
  process.exitCode = 1
} else {
  const lines = items.map((it) =>
    it.image
      ? `  { id: '${slug(it.name)}', name: ${JSON.stringify(it.name)}, image: ${JSON.stringify(it.image)} },`
      : `  { id: '${slug(it.name)}', name: ${JSON.stringify(it.name)} },`,
  )

  const ts =
    `import type { CollectionItem } from '@/types/achievement'\n\n` +
    `// AUTO-GENERATED by scripts/fetch-collection.mjs (Category:${category}) — ${items.length} items.\n` +
    `// Source: Coral Island Wiki (Fandom). Images hotlinked from the Fandom CDN.\n` +
    `export const ${varName}: CollectionItem[] = [\n` +
    `${lines.join('\n')}\n` +
    `]\n`

  mkdirSync(resolve(root, 'src/data/items'), { recursive: true })
  writeFileSync(resolve(root, `src/data/items/${key}.ts`), ts)
  const withImg = items.filter((i) => i.image).length
  console.log(`OK ${key}.ts (export ${varName}) — ${items.length} items (${withImg} with images).`)
}
